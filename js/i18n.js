let currentLanguage = localStorage.getItem('hms_lang') || 'en';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('hms_lang', lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    updateContent();
}

function updateContent() {
    document.title = translations[currentLanguage].title || document.title;
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[currentLanguage][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[currentLanguage][key];
            } else if (element.tagName === 'IMG') {
                element.alt = translations[currentLanguage][key];
            } else {
                element.innerText = translations[currentLanguage][key];
            }
        }
    });

    // Update section titles if on admin dashboard
    if (window.location.pathname.includes('admin-dashboard.html')) {
        const titleEl = document.getElementById('section-title');
        if (titleEl) {
            const currentSection = titleEl.getAttribute('data-i18n') || 'overview';
            titleEl.innerText = translations[currentLanguage][currentSection] || titleEl.innerText;
        }
    }

    // Handle Bootstrap RTL
    const bootstrapLink = document.getElementById('bootstrap-link');
    if (currentLanguage === 'ar') {
        bootstrapLink?.setAttribute('href', 'https://cdn.jsdelivr.net/npm/bootstrap@5.3.0/dist/css/bootstrap.rtl.min.css');
    } else {
        bootstrapLink?.setAttribute('href', 'https://cdn.jsdelivr.net/npm/bootstrap@5.3.0/dist/css/bootstrap.min.css');
    }
}

document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLanguage);
});

// Export translations for use in other scripts
window.translations = translations;
window.currentLanguage = currentLanguage;
window.setLanguage = setLanguage;
